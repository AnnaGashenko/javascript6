// Написать код который посчитает сумму всех элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////
const array = [1, 2, 3, 4];

var result = 0;
for (const item of array) {
    result += item; // result = result + item
}
